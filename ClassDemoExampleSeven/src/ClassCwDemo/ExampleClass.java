package ClassCwDemo;

public class ExampleClass 
{
    String name;
    int age;
    
    public void define()
    {
        name = "Aditya";
        age = 21;
       
    }
    
    public void display()
    {
        System.out.println("Name is "+name);
        System.out.println("Age is "+age);
        
        
    }
    
}
